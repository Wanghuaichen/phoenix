void bsp_Init(void)
{
    delay();//时钟初始化以后先等待一段时间，以等待系统稳定
	/* 优先级分组设置为4 */
	NVIC_PriorityGroupConfig(NVIC_PriorityGroup_4);
	
	bsp_InitUart();		/* 初始化串口驱动 */
	hal_beep_init();
    i2c1_init();
    WS2812_Timer_Init();
    hal_acc_init();
    hal_nfc_init();
    hal_dismantle_int_init();
}

